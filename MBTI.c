int getAnswer(char *msg, int N)
{
    char str[256];
    int answer;

    do {
        printf("%s", msg);
        gets(str);

        answer = toupper(str[0]) - 'A';
    } while (answer < 0 || answer >= N || strlen(str) > 1);

    return answer;
}

